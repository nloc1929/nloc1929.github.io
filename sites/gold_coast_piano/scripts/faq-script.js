/* JAVASCRIPT CODE TO DISPLAY FAQ ANSWERS */

// BUTTON 1:
function addText1() {
  // VARIABLES FOR [CONTAINER 1]: SELECTS ACCORDION CONTAINER DIV AND P ELEMENTS
  let accordDiv = document.querySelector('.accordDiv1');
  let accordText = document.querySelector('.accordText1');
  // CREATES [CONTAINER 1]: SETS THE DIV AND P ELEMENTS TO VISIBLE, CHANGES IMAGE
  accordDiv.setAttribute('style', 'display: block');
  accordText.setAttribute('style', 'display: block; visibility: visible');
  document.getElementById('arrow1').src="images/navigate-up-arrow.png";
}

